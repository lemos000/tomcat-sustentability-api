package br.com.fiap.beans;

public class RegiaoSustentavel {
    private int id;
    private String nome;

    public RegiaoSustentavel() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}